#include <iostream>
using namespace std;
int main(){
    int num1;
    int num2;
    cout<<"Enter two intergers with space between them: ";
    cin>>num1>>num2;
    float num3;
    float num4;
    cout<<"Enter two floats with space between them: ";
    cin>>num3>>num4;
    cout<<"Sum of two integers "<<num1<<" and "<<num2<<" is: "<<num1+num2<<endl;
    cout<<"Difference of two integers "<<num1<<" and "<<num2<<" is: "<<num1-num2<<endl;
    cout<<"Sum of two floats "<<num3<<" and "<<num4<<" is: "<<num3+num4<<endl;
    cout<<"Difference of two floats "<<num3<<" and "<<num4<<" is: "<<num3-num4<<endl;
}